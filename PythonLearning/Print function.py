# *args - Unlimited number of arguments

print("Hello", "World", "true", "123", "3.14")
# sep - how you want to separate arguments
print("Hello", "World", "true", "123", "3.14", sep="-")
# end - how you want to end the line
print("Hello", "World", "true", "123", "3.14", end=" ")
print("Hello", "World", "true", "123", "3.14", sep="-", end=" ")

print("I am a good person", end="_")
print("I am a bad person", end="_")
